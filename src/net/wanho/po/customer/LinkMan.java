package net.wanho.po.customer;

import java.util.Date;

/**
 * 联系人
 * @author Administrator
 *
 */
public class LinkMan {
	/**联系人编码*/
	private Integer linkManId;
	/**姓名*/
	private String name;
	/**性别*/
	private String sex;
	/**职位*/
	private String position;
	/**尊称*/
	private String nickName;
	/**电话*/
	private Integer phoneNum;
	/**邮箱*/
	private String email;
	/**QQ*/
	private String QQ;
	/**创建时间*/
	private Date createTime;
	/**更新时间*/
	private Date updateTime;
	/**备注*/
	private String remarks;
	/**二维码*/
	private String qrCode;
	/**客户编码*/
	private Integer customerId;
	
	

	public LinkMan() {
		super();
		// TODO Auto-generated constructor stub
	}

	public LinkMan(Integer linkManId, String name, String sex, String position, String nickName, Integer phoneNum,
			String email, String qQ, Date createTime, Date updateTime, String remarks, String qrCode,
			Integer customerId) {
		super();
		this.linkManId = linkManId;
		this.name = name;
		this.sex = sex;
		this.position = position;
		this.nickName = nickName;
		this.phoneNum = phoneNum;
		this.email = email;
		QQ = qQ;
		this.createTime = createTime;
		this.updateTime = updateTime;
		this.remarks = remarks;
		this.qrCode = qrCode;
		this.customerId = customerId;
	}

	public Integer getLinkManId() {
		return linkManId;
	}

	public void setLinkManId(Integer linkManId) {
		this.linkManId = linkManId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public String getPosition() {
		return position;
	}

	public void setPosition(String position) {
		this.position = position;
	}

	public String getNickName() {
		return nickName;
	}

	public void setNickName(String nickName) {
		this.nickName = nickName;
	}

	public Integer getPhoneNum() {
		return phoneNum;
	}

	public void setPhoneNum(Integer phoneNum) {
		this.phoneNum = phoneNum;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getQQ() {
		return QQ;
	}

	public void setQQ(String qQ) {
		QQ = qQ;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public Date getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}

	public String getRemarks() {
		return remarks;
	}

	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}

	public String getQrCode() {
		return qrCode;
	}

	public void setQrCode(String qrCode) {
		this.qrCode = qrCode;
	}
	
	public Integer getCustomerId() {
		return customerId;
	}

	public void setCustomerId(Integer customerId) {
		this.customerId = customerId;
	}

	@Override
	public String toString() {
		return "LinkMan [linkManId=" + linkManId + ", name=" + name + ", sex=" + sex + ", position=" + position
				+ ", nickName=" + nickName + ", phoneNum=" + phoneNum + ", email=" + email + ", QQ=" + QQ
				+ ", createTime=" + createTime + ", updateTime=" + updateTime + ", remarks=" + remarks + ", qrCode="
				+ qrCode + ", customerId=" + customerId + "]";
	}


	
	
}
